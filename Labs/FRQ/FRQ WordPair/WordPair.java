public class WordPair
{
    public String firstInput;
    public String secondInput;
    
    public WordPair (String first, String second){
        firstInput = first;
        secondInput = second;
    }

    public String getFirst(){
        return firstInput;
    }
    
    public String getSecond(){
        return secondInput;
    }
}
